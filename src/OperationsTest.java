
public class OperationsTest 
{

	public static void main(String[] args)
	{
		int number; 
		number = 20;
		number = number + 5;
		System.out.println(number);
		number/=2;
		System.out.println(number);
		number-=6;
		System.out.println(number);
		number*=4;
		System.out.println(number);
		number%=2;
		System.out.println(number);

	}

}
